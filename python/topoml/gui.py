def _diagram_rows(diagram: PersistenceDiagram | None) -> str:
    if diagram is None:
        return "<p>No persistence diagram supplied.</p>"
    rows = "\n".join(
        f"<tr><td>{pair.dimension}</td><td>{pair.birth:g}</td><td>{'inf' if pair.death is None else f'{pair.death:g}'}</td></tr>"
        for pair in diagram.pairs
    )
    return f"<table><thead><tr><th>Dimension</th><th>Birth</th><th>Death</th></tr></thead><tbody>{rows}</tbody></table>"


def _matrix_rows(feature_matrix: np.ndarray | None) -> str:
    if feature_matrix is None:
        return "<p>No feature matrix supplied.</p>"
    matrix = np.asarray(feature_matrix, dtype=float)
    rows = "\n".join(
        "<tr>" + "".join(f"<td>{value:g}</td>" for value in row) + "</tr>" for row in matrix
    )
    headers = "".join(f"<th>f{idx}</th>" for idx in range(matrix.shape[1] if matrix.ndim == 2 else 0))
    return f"<table><thead><tr>{headers}</tr></thead><tbody>{rows}</tbody></table>"


def _finite_bar_count(diagram: PersistenceDiagram | None) -> int:
    return 0 if diagram is None else len(diagram.finite_pairs())


def _matrix_shape(feature_matrix: np.ndarray | None) -> tuple[int, int]:
    if feature_matrix is None:
        return (0, 0)
    matrix = np.asarray(feature_matrix)
    if matrix.ndim != 2:
        return (0, 0)
    return (int(matrix.shape[0]), int(matrix.shape[1]))
