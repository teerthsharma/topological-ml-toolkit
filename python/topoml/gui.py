from __future__ import annotations

import html
import json
from pathlib import Path
from typing import Mapping

import numpy as np

from .core import PersistenceDiagram


def write_dashboard(
    path: str | Path,
    *,
    title: str,
    diagram: PersistenceDiagram | None = None,
    feature_matrix: np.ndarray | None = None,
    metadata: Mapping[str, object] | None = None,
) -> Path:
    """Write a self-contained HTML topology inspection dashboard."""

    target = Path(path)
    target.parent.mkdir(parents=True, exist_ok=True)
    target.write_text(
        _dashboard_html(
            title=title,
            diagram=diagram,
            feature_matrix=feature_matrix,
            metadata=metadata or {},
        ),
        encoding="utf-8",
    )
    return target


def _dashboard_html(
    *,
    title: str,
    diagram: PersistenceDiagram | None,
    feature_matrix: np.ndarray | None,
    metadata: Mapping[str, object],
) -> str:
    safe_title = html.escape(title)
    diagram_rows = _diagram_rows(diagram)
    matrix_rows = _matrix_rows(feature_matrix)
